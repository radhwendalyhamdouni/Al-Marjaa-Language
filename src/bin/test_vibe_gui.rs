// ═══════════════════════════════════════════════════════════════════════════════
// اختبار Vibe GUI - لغة المرجع
// ═══════════════════════════════════════════════════════════════════════════════
// تحويل النص العربي الطبيعي إلى واجهات GUI
// ═══════════════════════════════════════════════════════════════════════════════

use std::fs;
use std::path::Path;

fn main() {
    println!("╔══════════════════════════════════════════════════════════════╗");
    println!("║     🎨 اختبار Vibe GUI - لغة المرجع                         ║");
    println!("║     إنشاء واجهات باللغة الطبيعية العربية                    ║");
    println!("╚══════════════════════════════════════════════════════════════╝");
    println!();

    // استخدام المحرك من المكتبة
    use almarjaa::gui::{VibeGUIEngine};
    
    // ═══════════════════════════════════════════════════════════════════════════
    // اختبارات Vibe GUI
    // ═══════════════════════════════════════════════════════════════════════════
    
    let test_cases = vec![
        // مستوى أساسي
        ("أنشئ نافذة عنوانها مشروعي", "نافذة"),
        ("أضف زر مكتوب عليه تأكيد", "زر"),
        ("أضف حقل نص تلميح أدخل الاسم", "حقل نص"),
        ("أضف تسمية نصها مرحبا", "تسمية"),
        
        // مستوى متوسط
        ("أضف زر مكتوب عليه حفظ باللون الأخضر", "زر ملون"),
        ("أضف زر عليه حذف مع أنيميشين اهتزاز", "زر متحرك"),
        ("أضف حاوية في الموقع س 100 ص 50", "حاوية"),
        ("أضف قائمة منسدلة في الموقع س 200 ص 100", "قائمة"),
        
        // مستوى متقدم
        ("أضف منزلق من 0 إلى 100", "منزلق"),
        ("أضف شريط تقدم عرضه 300", "شريط تقدم"),
        ("أضف مربع اختيار نصه تذكرني", "مربع اختيار"),
    ];

    println!("┌──────────────────────────────────────────────────────────────┐");
    println!("│              🧪 اختبارات Vibe GUI                           │");
    println!("└──────────────────────────────────────────────────────────────┘");
    println!();

    let mut passed = 0;
    let mut failed = 0;

    for (input, category) in &test_cases {
        println!("━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━");
        println!("🏷️  النوع: {}", category);
        println!("🗣️  المدخل: \"{}\"", input);
        
        let mut engine = VibeGUIEngine::new();
        match engine.text_to_gui(input) {
            Ok(code) => {
                println!("✅ النتيجة: {}", code);
                passed += 1;
            }
            Err(e) => {
                println!("❌ خطأ: {}", e);
                failed += 1;
            }
        }
        println!();
    }

    // ═══════════════════════════════════════════════════════════════════════════
    // إنشاء واجهة كاملة - نموذج تسجيل
    // ═══════════════════════════════════════════════════════════════════════════
    
    println!("╔══════════════════════════════════════════════════════════════╗");
    println!("║              🏗️ إنشاء واجهة كاملة                           ║");
    println!("╚══════════════════════════════════════════════════════════════╝");
    println!();

    println!("📝 إنشاء نموذج تسجيل...");
    
    let mut full_engine = VibeGUIEngine::new();
    
    let commands = vec![
        "أنشئ نافذة عنوانها نموذج التسجيل",
        "أضف تسمية نصها الاسم",
        "أضف حقل نص تلميح أدخل اسمك",
        "أضف تسمية نصها البريد الإلكتروني",
        "أضف حقل نص تلميح example@email.com",
        "أضف تسمية نصها كلمة المرور",
        "أضف حقل نص تلميح أدخل كلمة المرور",
        "أضف مربع اختيار نصه أوافق على الشروط",
        "أضف زر مكتوب عليه تسجيل باللون الأخضر",
    ];

    for cmd in &commands {
        match full_engine.text_to_gui(cmd) {
            Ok(code) => println!("  ✅ {}", code),
            Err(e) => println!("  ❌ {}", e),
        }
    }
    println!();

    // حفظ HTML
    let html = full_engine.get_html();
    let output_path = Path::new("output/gui_demo.html");
    
    if let Some(parent) = output_path.parent() {
        let _ = fs::create_dir_all(parent);
    }
    
    match fs::write(output_path, &html) {
        Ok(_) => {
            println!("💾 تم حفظ الواجهة في: output/gui_demo.html");
        }
        Err(e) => {
            println!("❌ فشل الحفظ: {}", e);
        }
    }
    println!();

    // ═══════════════════════════════════════════════════════════════════════════
    // إنشاء واجهة لوحة تحكم
    // ═══════════════════════════════════════════════════════════════════════════
    
    println!("╔══════════════════════════════════════════════════════════════╗");
    println!("║              📊 إنشاء لوحة تحكم                              ║");
    println!("╚══════════════════════════════════════════════════════════════╝");
    println!();

    let mut dashboard_engine = VibeGUIEngine::new();
    
    let dashboard_commands = vec![
        "أنشئ نافذة عنوانها لوحة التحكم",
        "أضف تسمية نصها الإحصائيات",
        "أضف حاوية باللون الأخضر",
        "أضف تسمية نصها المستخدمون النشطون",
        "أضف تسمية نصها 1234",
        "أضف حاوية باللون الأزرق",
        "أضف تسمية نصها المبيعات",
        "أضف شريط تقدم",
        "أضف منزلق",
        "أضف زر مكتوب عليه تحديث البيانات باللون الأخضر",
        "أضف زر مكتوب عليه تصدير التقرير باللون الأزرق",
    ];

    for cmd in &dashboard_commands {
        match dashboard_engine.text_to_gui(cmd) {
            Ok(code) => println!("  ✅ {}", code),
            Err(e) => println!("  ❌ {}", e),
        }
    }
    println!();

    // حفظ لوحة التحكم
    let dashboard_html = dashboard_engine.get_html();
    let dashboard_path = Path::new("output/dashboard.html");
    
    match fs::write(dashboard_path, &dashboard_html) {
        Ok(_) => {
            println!("💾 تم حفظ لوحة التحكم في: output/dashboard.html");
        }
        Err(e) => {
            println!("❌ فشل الحفظ: {}", e);
        }
    }
    println!();

    // ═══════════════════════════════════════════════════════════════════════════
    // إنشاء واجهة مع أنيميشن
    // ═══════════════════════════════════════════════════════════════════════════
    
    println!("╔══════════════════════════════════════════════════════════════╗");
    println!("║              ✨ إنشاء واجهة مع أنيميشن                       ║");
    println!("╚══════════════════════════════════════════════════════════════╝");
    println!();

    let mut anim_engine = VibeGUIEngine::new();
    
    let anim_commands = vec![
        "أنشئ نافذة عنوانها واجهة متحركة",
        "أضف زر مكتوب عليه ظهور مع أنيميشين ظهور",
        "أضف زر مكتوب عليه انزلاق مع أنيميشين انزلاق",
        "أضف زر مكتوب عليه تكبير مع أنيميشين تكبير",
        "أضف زر مكتوب عليه دوران مع أنيميشين دوران",
        "أضف زر مكتوب عليه اهتزاز مع أنيميشين اهتزاز",
        "أضف زر مكتوب عليه نط مع أنيميشين نط",
        "أضف زر مكتوب عليه نبض مع أنيميشين نبض",
        "أضف زر مكتوب عليه وميض مع أنيميشين وميض",
    ];

    for cmd in &anim_commands {
        match anim_engine.text_to_gui(cmd) {
            Ok(code) => println!("  ✅ {}", code),
            Err(e) => println!("  ❌ {}", e),
        }
    }
    println!();

    // حفظ الواجهة المتحركة
    let anim_html = anim_engine.get_html();
    let anim_path = Path::new("output/animated_gui.html");
    
    match fs::write(anim_path, &anim_html) {
        Ok(_) => {
            println!("💾 تم حفظ الواجهة المتحركة في: output/animated_gui.html");
        }
        Err(e) => {
            println!("❌ فشل الحفظ: {}", e);
        }
    }
    println!();

    // ═══════════════════════════════════════════════════════════════════════════
    // النتائج النهائية
    // ═══════════════════════════════════════════════════════════════════════════
    
    println!("╔══════════════════════════════════════════════════════════════╗");
    println!("║                    📊 النتائج النهائية                      ║");
    println!("╠══════════════════════════════════════════════════════════════╣");
    println!("║  ✅ ناجح: {}                                                ║", passed);
    println!("║  ❌ فاشل: {}                                                ║", failed);
    let total = passed + failed;
    if total > 0 {
        println!("║  📈 النسبة: {:.1}%                                          ║", (passed as f32 / total as f32 * 100.0));
    }
    println!("╠══════════════════════════════════════════════════════════════╣");
    println!("║  📁 الملفات المُنتجة:                                        ║");
    println!("║     • output/gui_demo.html (نموذج تسجيل)                   ║");
    println!("║     • output/dashboard.html (لوحة تحكم)                     ║");
    println!("║     • output/animated_gui.html (واجهة متحركة)               ║");
    println!("╚══════════════════════════════════════════════════════════════╝");
}
