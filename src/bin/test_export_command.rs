// ═══════════════════════════════════════════════════════════════════════════════
// اختبار أمر التصدير - لغة المرجع
// ═══════════════════════════════════════════════════════════════════════════════
// يختبر:
// - صدر البرنامج <اسم> على <منصة>
// - جميع المنصات المدعومة
// - تكامل التصدير مع نظام GUI
// ═══════════════════════════════════════════════════════════════════════════════

use std::fs;

fn main() {
    println!("╔══════════════════════════════════════════════════════════════╗");
    println!("║     📦 اختبار أمر التصدير - لغة المرجع                     ║");
    println!("╚══════════════════════════════════════════════════════════════╝");
    println!();

    let mut passed = 0;
    let mut total = 0;

    // ═══════════════════════════════════════════════════════════════════════════
    // اختبار 1: تصدير بسيط ويندوز
    // ═══════════════════════════════════════════════════════════════════════════
    
    total += 1;
    println!("📝 اختبار 1: تصدير بسيط - ويندوز");
    println!("━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━");
    
    use almarjaa::GGUFEngine;
    
    let engine = GGUFEngine::new();
    let result = engine.simulate_inference("صدر البرنامج حاسبة على ويندوز");
    
    println!("📥 المدخل: \"صدر البرنامج حاسبة على ويندوز\"");
    println!("📤 الناتج:");
    for line in result.lines() {
        println!("   {}", line);
    }
    
    if result.contains("حاسبة") && result.contains("windows") {
        println!("✅ نجح!");
        passed += 1;
    } else {
        println!("❌ فشل!");
    }
    println!();

    // ═══════════════════════════════════════════════════════════════════════════
    // اختبار 2: تصدير لينكس
    // ═══════════════════════════════════════════════════════════════════════════
    
    total += 1;
    println!("📝 اختبار 2: تصدير - لينكس");
    println!("━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━");
    
    let result = engine.simulate_inference("صدر البرنامج myapp على linux");
    
    println!("📥 المدخل: \"صدر البرنامج myapp على linux\"");
    println!("📤 الناتج:");
    for line in result.lines() {
        println!("   {}", line);
    }
    
    if result.contains("myapp") && result.contains("linux") {
        println!("✅ نجح!");
        passed += 1;
    } else {
        println!("❌ فشل!");
    }
    println!();

    // ═══════════════════════════════════════════════════════════════════════════
    // اختبار 3: تصدير ماك
    // ═══════════════════════════════════════════════════════════════════════════
    
    total += 1;
    println!("📝 اختبار 3: تصدير - ماك");
    println!("━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━");
    
    let result = engine.simulate_inference("صدر البرنامج مشغل_موسيقى على mac");
    
    println!("📥 المدخل: \"صدر البرنامج مشغل_موسيقى على mac\"");
    println!("📤 الناتج:");
    for line in result.lines() {
        println!("   {}", line);
    }
    
    if result.contains("مشغل_موسيقى") && result.contains("macos") {
        println!("✅ نجح!");
        passed += 1;
    } else {
        println!("❌ فشل!");
    }
    println!();

    // ═══════════════════════════════════════════════════════════════════════════
    // اختبار 4: تصدير ويب
    // ═══════════════════════════════════════════════════════════════════════════
    
    total += 1;
    println!("📝 اختبار 4: تصدير - ويب");
    println!("━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━");
    
    let result = engine.simulate_inference("صدر البرنامج لوحة_تحكم على web");
    
    println!("📥 المدخل: \"صدر البرنامج لوحة_تحكم على web\"");
    println!("📤 الناتج:");
    for line in result.lines() {
        println!("   {}", line);
    }
    
    if result.contains("لوحة_تحكم") && result.contains("web") {
        println!("✅ نجح!");
        passed += 1;
    } else {
        println!("❌ فشل!");
    }
    println!();

    // ═══════════════════════════════════════════════════════════════════════════
    // اختبار 5: تنفيذ التصدير الفعلي
    // ═══════════════════════════════════════════════════════════════════════════
    
    total += 1;
    println!("📝 اختبار 5: تنفيذ التصدير الفعلي");
    println!("━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━");
    
    use almarjaa::exporter::{export_html_only, export_project};
    
    let source_code = r#"أنشئ نافذة بعنوان "حاسبة الذكاء"
أضف حقل نص تلميح "الرقم الأول"
أضف حقل نص تلميح "الرقم الثاني"
أضف زر مكتوب عليه "احسب" باللون الأخضر
أضف تسمية نصها "النتيجة: ---""#;
    
    let result = export_html_only(source_code, "test_calculator");
    
    println!("📥 الكود المصدري:");
    for line in source_code.lines() {
        println!("   {}", line);
    }
    
    if result.success {
        println!("✅ نجح التصدير!");
        if let Some(path) = &result.output_path {
            println!("📁 الملف: {:?}", path);
            
            // التحقق من محتوى الملف
            if let Ok(content) = fs::read_to_string(path) {
                if content.contains("حاسبة") && content.contains("<button>") {
                    println!("✅ محتوى HTML صحيح!");
                    passed += 1;
                } else {
                    println!("❌ محتوى HTML غير صحيح!");
                }
            }
        }
    } else {
        println!("❌ فشل: {}", result.message);
    }
    println!();

    // ═══════════════════════════════════════════════════════════════════════════
    // اختبار 6: تصدير مشروع كامل
    // ═══════════════════════════════════════════════════════════════════════════
    
    total += 1;
    println!("📝 اختبار 6: تصدير مشروع كامل");
    println!("━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━");
    
    let full_source = r#"// برنامج مدير المهام
أنشئ نافذة بعنوان "مدير المهام"
أضف تسمية نصها "أدخل المهمة:"
أضف حقل نص تلميح "مهمة جديدة..."
أضف زر مكتوب عليه "إضافة" باللون الأخضر
أضف زر مكتوب عليه "حذف" باللون الأحمر
أضف حاوية عرض 400 ارتفاع 300"#;
    
    let result = export_project(full_source, "task_manager_full", "windows");
    
    if result.success {
        println!("✅ نجح التصدير!");
        if let Some(path) = &result.output_path {
            println!("📁 المسار: {:?}", path);
        }
        for warning in &result.warnings {
            println!("⚠️ {}", warning);
        }
        passed += 1;
    } else {
        println!("❌ فشل: {}", result.message);
    }
    println!();

    // ═══════════════════════════════════════════════════════════════════════════
    // اختبار 7: التكامل مع Vibe Coding
    // ═══════════════════════════════════════════════════════════════════════════
    
    total += 1;
    println!("📝 اختبار 7: التكامل مع Vibe Coding");
    println!("━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━");
    
    use almarjaa::integration::natural_to_app;
    
    let natural_text = "أريد حاسبة تجمع و تطرح رقمين";
    let result = natural_to_app(natural_text, "vibe_calculator");
    
    println!("📥 النص الطبيعي: \"{}\"", natural_text);
    
    if result.success {
        println!("✅ نجح التكامل!");
        println!("📝 كود AI:");
        for line in result.ai_code.lines() {
            println!("   {}", line);
        }
        if let Some(path) = &result.export_path {
            println!("📁 المسار: {:?}", path);
        }
        passed += 1;
    } else {
        println!("❌ فشل: {}", result.message);
    }
    println!();

    // ═══════════════════════════════════════════════════════════════════════════
    // النتائج النهائية
    // ═══════════════════════════════════════════════════════════════════════════
    
    println!("╔══════════════════════════════════════════════════════════════╗");
    println!("║                    📊 النتائج النهائية                      ║");
    println!("╠══════════════════════════════════════════════════════════════╣");
    println!("║  الاختبارات: {}/{} ناجحة                                    ║", passed, total);
    
    let success_rate = (passed as f32 / total as f32) * 100.0;
    println!("║  نسبة النجاح: {:.1}%                                     ║", success_rate);
    
    if passed == total {
        println!("║                                                              ║");
        println!("║  🎉 جميع الاختبارات ناجحة!                                  ║");
    }
    
    println!("╠══════════════════════════════════════════════════════════════╣");
    println!("║  صيغ التصدير المدعومة:                                      ║");
    println!("║     • HTML - تطبيق ويب مستقل                                ║");
    println!("║     • Rust Project - مشروع Rust كامل                        ║");
    println!("║     • Windows/Linux/macOS/Web                               ║");
    println!("╚══════════════════════════════════════════════════════════════╝");
}
